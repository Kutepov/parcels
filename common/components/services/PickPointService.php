    public function track($trackNumber, $extraFields = [])
    {
        if (!($recaptcha = $this->preheatCaptcha())) {
            return false;
        }

        return $this->request($trackNumber, $recaptcha->token, $extraFields)->wait();
    }

    public function trackAsync($trackNumber, $extraFields = []): PromiseInterface
    {
        if (!($token = \common\models\redis\Recaptcha::findTokenForProvider($this->id))) {
            throw new BadPreheatedCaptchaException();
        }

        return $this->request($trackNumber, $token, $extraFields);
    }

    public function parseResponse($response, $trackNumber, $extraInfo = [])
    {
        $body = $response->getBody()->getContents();
        $result = new Parcel();

        if (preg_match('#<td>Терминал доставки :(.*?)</td>#ui', $extraInfo['body'], $m)) {
            $result->destinationAddress = str_replace('<br>', ', ', trim($m[1]));
        }

        preg_match_all('#<tr><td>(.*?)</td><td>(.*?)</td></tr>#siu', $body, $m, PREG_SET_ORDER);

        foreach ($m as $checkpoint) {
            $date = Carbon::parse($checkpoint[2]);

            $result->statuses[] = new Status([
                'title' => trim($checkpoint[1]),
                'date' => $date->timestamp,
                'dateVal' => $date->toDateString(),
                'timeVal' => $date->toTimeString('minute')
            ]);
        }

        return $result;
    }

    private function request($trackNumber, $token, $extraFields = [])
    {
        return $this->sendAsyncRequestWithProxy(new Request('POST', 'https://pickpoint.ru/monitoring/msearch.php'), $trackNumber, [
            RequestOptions::FORM_PARAMS => [
                'shop' => self::SHOPS[$extraFields['extra_shop']],
                'order_number' => $trackNumber,
                'token' => $token,
                'g-recaptcha-response' => $token
            ]
        ], function (ResponseInterface $response) use ($trackNumber) {
            $body = $response->getBody()->getContents();

            preg_match('#monitoring\.show_history\(&quot;([\d]+)&quot;, &quot;([\d]+)&quot;\)#i', $body, $m);

            if (!isset($m[1])) {
                return false;
            }

            return $this->sendAsyncRequestWithProxy(new Request('GET', 'https://pickpoint.ru/monitoring/mhistory.php'), $trackNumber, [
                RequestOptions::QUERY => [
                    'id' => $m[1],
                    'owner_id' => $m[2]
                ],
                BaseService::EXTRA_INFO => [
                    'body' => $body
                ]
            ]);
        });
    }

